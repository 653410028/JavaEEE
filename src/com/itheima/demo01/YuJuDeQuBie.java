package com.itheima.demo01;

public class YuJuDeQuBie {
    public static void main(String[] args) {
        for (int i = 1; i < 6; i++) {
            System.out.println("好好学习，天天向上");
        }
            System.out.println("---------------");
        int w = 1;
        while (w < 6){
            System.out.println("好好学习，天天向上");
            w++;
        }
            System.out.println("---------------");
        int d = 1;
        do {
            System.out.println("好好学习，天天向上");
            d++;
        }while (d < 6);
    }
}